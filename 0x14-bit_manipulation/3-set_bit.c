/**
 * set_bit - Function sets the value of a bit to 1 at a given index.
 * @n: Pointer to n
 * @index: The index, starting from 0 of the bit to set.
 *
 * Description: Function sets the value of a bit to 1 at a given index.
 *
 * Return: 1 if it worked, -1 otherwise.
 */
int set_bit(unsigned long int *n, unsigned int index)
{
	while (index)
	{
		index = 1;
		index--;
	}
	return (1);
}
