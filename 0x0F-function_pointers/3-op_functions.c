#include "3-calc.h"
/**
 * op_add - Adds two integers together.
 * @a: First integer
 * @b: Second integer
 *
 * Description: Adds 2 integers together.
 * Return: Value of the addition.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtracts integer b from integer a
 * @a: First integer
 * @b: Second integer
 *
 * Description: Subtracts integer b from integer a
 *
 * Return: Value of the subtraction operation.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies two integers.
 * @a: First integer.
 * @b: Second integer.
 *
 * Description: Multiplies 2 integers.
 *
 * Return: Value of the multiplication operation.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides integers.
 * @a: First integer.
 * @b: Second integer.
 *
 * Description: Divides 2 integers.
 *
 * Return: Value of the division operation.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Performs modulo operation
 * @a: First integer.
 * @b: Second integer.
 *
 * Description: Performs modulo operation.
 *
 * Return: Value of the modulo operation.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
